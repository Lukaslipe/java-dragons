    public boolean perguntarSimNao(String pergunta) {
        while (true) {
            System.out.println(pergunta);
            System.out.print("Digite 'sim' ou 'não': ");
            String resposta = scanner.nextLine().trim().toLowerCase();

            if (resposta.equals("sim")) return true;
            if (resposta.equals("não")) return false;

            System.out.println("Resposta inválida! Por favor, digite 'sim' ou 'não'.");
        }
    }

    public static void esperar(int milissegundos) {
        try {
            Thread.sleep(milissegundos);
        } catch (InterruptedException e) {
            // Caso algo interrompa a pausa, apenas continua
            Thread.currentThread().interrupt();
            System.out.println("Pausa interrompida!");
        }
    }
